#include <stdio.h>

int b_operadores_incremento(void) { //b_operadores_incremento

    int numero_1 = 0;

    //Aumenta en una unidad el valor de una variable numerica.
    numero_1++;
    ++numero_1;
    numero_1 += 1;

    printf("La variable incrementada es igual a: %d", numero_1);

    return 0;
}
















